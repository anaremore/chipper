std::vector<std::string> presetTagsFor(const PresetInfo& preset)
{
    std::vector<std::string> tags;
    addPresetTag(tags, "factory");
    addPresetTag(tags, toString(preset.chip));
    addPresetTag(tags, toString(preset.macro));
    addPresetTag(tags, presetRoleFor(preset));
    addPresetTag(tags, presetEngineFor(preset));

    switch (preset.chip)
    {
        case ChipMode::nes:
        case ChipMode::nesVrc6:
        case ChipMode::nesFds:
        case ChipMode::nesSunsoft5b:
        case ChipMode::nesMmc5:
        case ChipMode::nesVrc7:
        case ChipMode::dmg:
            addPresetTag(tags, "apu");
            if (preset.chip == ChipMode::nesVrc6)
            {
                addPresetTag(tags, "vrc6");
                addPresetTag(tags, "expansion");
            }
            if (preset.chip == ChipMode::nesFds)
            {
                addPresetTag(tags, "fds");
                addPresetTag(tags, "wavetable");
                addPresetTag(tags, "modulation");
                addPresetTag(tags, "expansion");
            }
            if (preset.chip == ChipMode::nesSunsoft5b)
            {
                addPresetTag(tags, "sunsoft");
                addPresetTag(tags, "5b");
                addPresetTag(tags, "psg");
                addPresetTag(tags, "square");
                addPresetTag(tags, "expansion");
            }
            if (preset.chip == ChipMode::nesMmc5)
            {
                addPresetTag(tags, "mmc5");
                addPresetTag(tags, "square");
                addPresetTag(tags, "pcm");
                addPresetTag(tags, "dac");
                addPresetTag(tags, "expansion");
            }
            if (preset.chip == ChipMode::nesVrc7)
            {
                addPresetTag(tags, "vrc7");
                addPresetTag(tags, "opll");
                addPresetTag(tags, "fm");
                addPresetTag(tags, "operator");
                addPresetTag(tags, "expansion");
            }
            break;
        case ChipMode::ym2149:
        case ChipMode::sn76489:
        case ChipMode::saa1099:
            addPresetTag(tags, "psg");
            if (preset.chip == ChipMode::saa1099)
            {
                addPresetTag(tags, "saa1099");
                addPresetTag(tags, "stereo");
                addPresetTag(tags, "envelope");
            }
            break;
        case ChipMode::ym2612:
        case ChipMode::opl3:
        case ChipMode::ym2151:
        case ChipMode::ym2413:
        case ChipMode::ym2203:
        case ChipMode::ym2608:
        case ChipMode::ym2610:
            addPresetTag(tags, "fm");
            addPresetTag(tags, "operator");
            addPresetTag(tags, "envelope");
            if (preset.chip == ChipMode::ym2203 || preset.chip == ChipMode::ym2608 || preset.chip == ChipMode::ym2610)
                addPresetTag(tags, "ssg");
            if (preset.chip == ChipMode::ym2608)
                addPresetTag(tags, "opna");
            if (preset.chip == ChipMode::ym2610)
            {
                addPresetTag(tags, "opnb");
                addPresetTag(tags, "neo");
            }
            break;
        case ChipMode::spc700:
        case ChipMode::paula:
            addPresetTag(tags, "sampler");
            break;
        case ChipMode::huc6280:
        case ChipMode::namcoWsg:
        case ChipMode::scc:
            addPresetTag(tags, "wavetable");
            break;
        case ChipMode::sid:
            addPresetTag(tags, "sid");
            break;
        case ChipMode::pokey:
            addPresetTag(tags, "pokey");
            break;
    }

    if ((preset.chip == ChipMode::nes
         || preset.chip == ChipMode::nesVrc6
         || preset.chip == ChipMode::nesFds
         || preset.chip == ChipMode::nesSunsoft5b
         || preset.chip == ChipMode::nesMmc5
         || preset.chip == ChipMode::nesVrc7)
        && preset.nesDmcDirectLevel > 0.0001f)
    {
        addPresetTag(tags, "dmc");
        addPresetTag(tags, "dac");
    }

    if (preset.chip == ChipMode::ym2149 && (preset.ymEnvelopeShape > 0 || preset.envelopeDecay > 0.0001f))
        addPresetTag(tags, "envelope");

    if (preset.chip == ChipMode::spc700
        && preset.macro != MacroKind::drum
        && preset.macro != MacroKind::hit)
        addPresetTag(tags, "loop");

    if (preset.chip == ChipMode::pokey
        && (preset.waveShape >= 3 || preset.macro == MacroKind::drum
            || preset.macro == MacroKind::hit || preset.macro == MacroKind::laser))
        addPresetTag(tags, "noise");

    const auto corpus = presetCorpus(preset);
    for (const auto token : { "pulse", "saw", "triangle", "duty", "stereo", "sweep", "wave", "wavetable", "mod", "modulation", "fds", "sunsoft", "5b", "mmc5", "pcm", "square", "tone", "noise", "periodic", "filter", "pwm", "sync", "ring", "dmc", "dac", "fm", "opna", "opnb", "ssg", "psg", "saa1099", "operator", "feedback", "echo", "loop", "tracker", "rhythm", "envelope", "chord", "bell", "pad", "rise", "alarm", "polynomial", "distortion", "lfo", "arcade", "neo" })
    {
        if (presetCorpusContains(corpus, token))
            addPresetTag(tags, token);
    }

    return tags;
}

